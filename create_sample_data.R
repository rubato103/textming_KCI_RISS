# create_sample_data.R
# 데모용 샘플 데이터 생성 스크립트
# 실제 데이터가 없을 때 테스트용으로 사용

cat("========== 샘플 데이터 생성 ==========\n")

# 패키지 로드
if (!require("writexl", quietly = TRUE)) {
  install.packages("writexl")
  library(writexl)
}

# 샘플 데이터 생성
sample_data <- data.frame(
  `논문 ID` = paste0("SAMPLE_", sprintf("%03d", 1:50)),
  저자 = c(rep("김교육", 10), rep("박연구", 15), rep("이분석", 10), 
           rep("정학습", 10), rep("최논문", 5)),
  제목 = c(
    rep("학습부진 학생을 위한 개별화 교육 방안 연구", 10),
    rep("온라인 교육 플랫폼의 효과성 분석", 15), 
    rep("초등학교 수학교육의 새로운 접근", 10),
    rep("디지털 교육 도구 활용 방안", 10),
    rep("교육격차 해소를 위한 정책 연구", 5)
  ),
  초록 = c(
    rep("본 연구는 학습부진 학생들을 대상으로 개별화 교육의 효과를 분석하였다. 연구 결과 맞춤형 학습이 학업성취도 향상에 긍정적 영향을 미치는 것으로 나타났다.", 10),
    rep("온라인 교육 플랫폼의 학습 효과를 실증적으로 분석한 연구이다. 디지털 환경에서의 상호작용이 학습 동기와 성취도에 미치는 영향을 조사하였다.", 15),
    rep("초등학교 수학교육에서 구체적 조작물을 활용한 새로운 교수법의 효과를 검증하였다. 학생들의 수학적 사고력 향상에 유의미한 결과를 보였다.", 10),
    rep("최신 디지털 교육 도구들의 교실 내 활용 방안을 제시하고 그 효과성을 평가하였다. 교사와 학생 모두에게 긍정적 반응을 확인하였다.", 10),
    rep("교육격차 문제의 원인을 분석하고 이를 해소하기 위한 정책적 방안을 제안하였다. 사회경제적 배경이 학습 성과에 미치는 영향을 중점적으로 다루었다.", 5)
  ),
  발행연도 = sample(2015:2024, 50, replace = TRUE),
  학술지명 = sample(c("한국교육학연구", "교육과정연구", "학습자중심교과교육연구", "교육심리연구"), 50, replace = TRUE),
  check.names = FALSE,
  stringsAsFactors = FALSE
)

# 디렉토리 생성
if (!dir.exists("data")) dir.create("data")
if (!dir.exists("data/raw_data")) dir.create("data/raw_data", recursive = TRUE)

# Excel 파일로 저장
sample_filename <- "data/raw_data/sample_data_for_demo.xlsx"
write_xlsx(sample_data, sample_filename)

cat(sprintf("✅ 샘플 데이터 생성 완료: %s\n", sample_filename))
cat(sprintf("📊 데이터 크기: %d행 %d열\n", nrow(sample_data), ncol(sample_data)))
